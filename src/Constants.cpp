#include "Constants.h"

int char_to_int(char c) {
  if (c == 'a') return 0;
  else if (c == 'b') return 1;
  else if (c == 'c') return 2;
  else if (c == 'd') return 3;
  else if (c == 'e') return 4;
  else if (c == 'f') return 5;
  else if (c == 'g') return 6;
  else if (c == 'h') return 7;
}
